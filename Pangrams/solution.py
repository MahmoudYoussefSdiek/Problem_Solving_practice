def pangrams(s):
    s = s.lower()
    unique_chars = set()

    for character in s:
        if character.isalpha():
            unique_chars.add(character)

    if len(unique_chars) == 26:
        return "pangram"
    else:
        return "not pangram"

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    s = input()

    result = pangrams(s)

    fptr.write(result + '\n')

    fptr.close()